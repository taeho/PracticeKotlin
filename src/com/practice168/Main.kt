package com.practice168.ex_max_min_function
// 최대값 , 최소값 구하기, max와 min 함수로 확인

import kotlin.math.max	// 해당 패키지 import, max 출력
import kotlin.math.min	// 최소값 출력 위한 import

fun main(args: Array<String>)
{
	println(max(3, 7))		// 출력: 7
	println(max(10.53, 1.15))	// 출력: 10.53
	println(min(18, 13))	// 출력: 13
	println(min(7.6, 20.01))	// 출력: 7.6
}
